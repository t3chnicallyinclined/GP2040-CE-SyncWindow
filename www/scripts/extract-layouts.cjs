#!/usr/bin/env node
/**
 * Extract button layout coordinate data from C header files and generate
 * a static JS data module for SVG preview rendering in the web UI.
 *
 * Usage: node scripts/extract-layouts.js > src/Data/ButtonLayoutData.js
 *
 * Parses:
 *   - headers/buttonlayouts.h — BUTTON_GROUP_* macro definitions
 *   - src/layoutmanager.cpp — maps layout enum values to macro names
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const LAYOUTS_H = path.join(ROOT, 'headers', 'buttonlayouts.h');
const LAYOUT_MGR = path.join(ROOT, 'src', 'layoutmanager.cpp');

// Element type enum values (from proto/enums.proto)
const GP_ELEMENT = {
	GP_ELEMENT_BTN_BUTTON: 2,
	GP_ELEMENT_DIR_BUTTON: 3,
	GP_ELEMENT_LEVER: 5,
};

// Shape type enum values
const GP_SHAPE = {
	GP_SHAPE_ELLIPSE: 0,
	GP_SHAPE_SQUARE: 1,
};

// Gamepad masks (not needed for rendering, but included for completeness)
const GAMEPAD_MASKS = {
	GAMEPAD_MASK_UP: 0x0001, GAMEPAD_MASK_DOWN: 0x0002,
	GAMEPAD_MASK_LEFT: 0x0004, GAMEPAD_MASK_RIGHT: 0x0008,
	GAMEPAD_MASK_B1: 0x0010, GAMEPAD_MASK_B2: 0x0020,
	GAMEPAD_MASK_B3: 0x0040, GAMEPAD_MASK_B4: 0x0080,
	GAMEPAD_MASK_L1: 0x0100, GAMEPAD_MASK_R1: 0x0200,
	GAMEPAD_MASK_L2: 0x0400, GAMEPAD_MASK_R2: 0x0800,
	GAMEPAD_MASK_S1: 0x1000, GAMEPAD_MASK_S2: 0x2000,
	GAMEPAD_MASK_L3: 0x4000, GAMEPAD_MASK_R3: 0x8000,
	GAMEPAD_MASK_A1: 0x10000, GAMEPAD_MASK_A2: 0x20000,
};

function resolveConstant(val) {
	val = val.trim();
	if (GP_ELEMENT[val] !== undefined) return GP_ELEMENT[val];
	if (GP_SHAPE[val] !== undefined) return GP_SHAPE[val];
	if (GAMEPAD_MASKS[val] !== undefined) return GAMEPAD_MASKS[val];
	const num = parseInt(val);
	if (!isNaN(num)) return num;
	return 0;
}

// Parse buttonlayouts.h — extract BUTTON_GROUP_* macros
function parseButtonGroups(src) {
	const groups = {};
	// Join continuation lines
	const joined = src.replace(/\\\n/g, ' ');
	// Match #define BUTTON_GROUP_XXX { ... }
	const re = /#define\s+(BUTTON_GROUP_\w+)\s+(\{[\s\S]*?\})\s*(?=#define|\n\n|$)/g;
	let m;
	while ((m = re.exec(joined)) !== null) {
		const name = m[1];
		const body = m[2];
		const elements = [];

		// Match each element: {TYPE, {params...}}
		const elemRe = /\{(GP_ELEMENT_\w+)\s*,\s*\{([^}]+)\}\}/g;
		let em;
		while ((em = elemRe.exec(body)) !== null) {
			const type = resolveConstant(em[1]);
			const params = em[2].split(',').map(resolveConstant);
			elements.push({ type, params });
		}

		if (elements.length > 0) {
			groups[name] = elements;
		}
	}
	return groups;
}

// Parse layoutmanager.cpp — map draw functions to macro names and enum cases
function parseLayoutMappings(src) {
	const leftLayouts = {};
	const rightLayouts = {};

	// Split source at function boundaries to avoid cross-matching
	const leftStart = src.indexOf('LayoutManager::getLeftLayout');
	const rightStart = src.indexOf('LayoutManager::getRightLayout');

	const caseRe = /case\s+(\w+):\s*\n\s*return\s+(?:this->)?draw(\w+)\(\)/g;

	if (leftStart >= 0 && rightStart >= 0) {
		// Left layout cases: between getLeftLayout and getRightLayout
		const leftSection = src.substring(leftStart, rightStart);
		let cm;
		while ((cm = caseRe.exec(leftSection)) !== null) {
			leftLayouts[cm[1]] = cm[2];
		}

		// Right layout cases: from getRightLayout to end (or next function)
		const rightSection = src.substring(rightStart);
		caseRe.lastIndex = 0;
		while ((cm = caseRe.exec(rightSection)) !== null) {
			rightLayouts[cm[1]] = cm[2];
		}
	}

	return { leftLayouts, rightLayouts };
}

// Map draw function names to BUTTON_GROUP macro names
function parseDrawFunctions(src) {
	const funcToMacro = {};
	const re = /LayoutManager::draw(\w+)\(\)\s*\{[\s\n]*return\s+(BUTTON_GROUP_\w+)/g;
	let m;
	while ((m = re.exec(src)) !== null) {
		funcToMacro[m[1]] = m[2];
	}
	return funcToMacro;
}

// Main
const layoutsSrc = fs.readFileSync(LAYOUTS_H, 'utf8');
const mgrSrc = fs.readFileSync(LAYOUT_MGR, 'utf8');

const buttonGroups = parseButtonGroups(layoutsSrc);
const { leftLayouts, rightLayouts } = parseLayoutMappings(mgrSrc);
const drawToMacro = parseDrawFunctions(mgrSrc);

// Build output data
function buildLayoutData(layoutMap, side) {
	const result = {};
	for (const [enumName, drawFunc] of Object.entries(layoutMap)) {
		const macroName = drawToMacro[drawFunc];
		if (macroName && buttonGroups[macroName]) {
			result[enumName] = buttonGroups[macroName];
		}
	}
	return result;
}

const leftData = buildLayoutData(leftLayouts, 'left');
const rightData = buildLayoutData(rightLayouts, 'right');

// Output as JS module
console.log('// Auto-generated by scripts/extract-layouts.js');
console.log('// DO NOT EDIT — regenerate with: node scripts/extract-layouts.js > src/Data/ButtonLayoutData.js');
console.log('//');
console.log('// Element types: 2=BTN_BUTTON, 3=DIR_BUTTON, 5=LEVER');
console.log('// Shape types: 0=ELLIPSE, 1=SQUARE');
console.log('// Params for LEVER: [x, y, radiusX, radiusY, stroke, fill, dpadMode]');
console.log('// Params for buttons: [x1, y1, x2/radiusX, y2/radiusY, stroke, fill, value, shape, ...]');
console.log('');
console.log('export const leftLayouts = ' + JSON.stringify(leftData, null, 2) + ';');
console.log('');
console.log('export const rightLayouts = ' + JSON.stringify(rightData, null, 2) + ';');
console.log('');
console.log(`// Total: ${Object.keys(leftData).length} left + ${Object.keys(rightData).length} right layouts`);
